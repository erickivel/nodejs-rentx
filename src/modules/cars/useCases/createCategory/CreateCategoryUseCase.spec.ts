describe("Create Category", () => {

    it("Should be able to create a category", () => {
        const soma = 2 + 2;

        const resultado = 4;

        expect(soma).toBe(resultado);
    });

    it("Should be able to create a category", () => {
        const soma = 2 + 2;

        const resultado = 5;

        expect(soma).not.toBe(resultado);
    });
});
